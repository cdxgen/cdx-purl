#!/usr/bin/env node

import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.join(__dirname, "..");
const TYPES_DIR = path.join(ROOT, "specification", "types");
const OUT_DIR = path.join(ROOT, "generated");
const OUT_FILE = path.join(OUT_DIR, "type-rules.js");

const NORMALIZATION_TEXT_TO_OP = Object.freeze([
  ["Replace underscore _ with dash -", "replace_underscore_with_dash"],
  ["Replace non-[a-z] letters, non-[0-9] digits with underscore _", "replace_non_alnum_with_underscore"]
]);

function deriveNormalizationOps(rules) {
  const ops = [];
  for (const rule of rules) {
    for (const [needle, op] of NORMALIZATION_TEXT_TO_OP) {
      if (String(rule).includes(needle) && !ops.includes(op)) {
        ops.push(op);
      }
    }
  }
  return ops;
}

function toRule(definition) {
  const normalizationRules = Array.isArray(definition?.normalization_rules) ? definition.normalization_rules : [];

  if (!definition) {
    return {
      requirement: "optional",
      caseSensitive: true,
      permittedCharacters: null,
      normalizationRules: [],
      normalizationOps: []
    };
  }

  return {
    requirement: definition.requirement ?? "optional",
    caseSensitive: definition.case_sensitive !== false,
    permittedCharacters: definition.permitted_characters ?? null,
    normalizationRules,
    normalizationOps: deriveNormalizationOps(normalizationRules)
  };
}

export function loadTypeRuleSource() {
  const files = readdirSync(TYPES_DIR)
    .filter((name) => name.endsWith("-definition.json"))
    .sort();

  const out = {};
  for (const fileName of files) {
    const definition = JSON.parse(readFileSync(path.join(TYPES_DIR, fileName), "utf8"));
    const qualifiers = [];

    for (const qualifier of definition.qualifiers_definition ?? []) {
      const key = String(qualifier.key || "").toLowerCase();
      if (!key) {
        continue;
      }
      qualifiers.push({
        key,
        requirement: qualifier.requirement ?? "optional",
        defaultValue: qualifier.default_value ?? null
      });
    }

    qualifiers.sort((a, b) => a.key.localeCompare(b.key));

    out[definition.type] = {
      namespace: toRule(definition.namespace_definition),
      name: toRule(definition.name_definition),
      version: toRule(definition.version_definition),
      subpath: toRule(definition.subpath_definition),
      qualifiers
    };
  }

  return out;
}

export function renderTypeRulesModule(source) {
  return `// This file is generated by scripts/generate-type-rules.mjs.\n// Do not edit manually.\n\nexport const TYPE_RULES_SOURCE = ${JSON.stringify(source, null, 2)};\n`;
}

export function generateTypeRulesFile() {
  const source = loadTypeRuleSource();
  const content = renderTypeRulesModule(source);
  mkdirSync(OUT_DIR, { recursive: true });
  writeFileSync(OUT_FILE, content, "utf8");
  return OUT_FILE;
}

if (process.argv[1] && path.resolve(process.argv[1]) === __filename) {
  const outFile = generateTypeRulesFile();
  console.log(`Wrote ${path.relative(ROOT, outFile)}`);
}


